        public static long Part2(string[] lines)
        {
            var points = new Dictionary<char, int>
            {
                {'(', 1},
                {'[', 2},
                {'{', 3},
                {'<', 4},
            };
            var scores = new List<long>();
            foreach (var l in lines)
            {
                var open = new Stack<char>();
                foreach (var c in l)
                {
                    char opening;
                    if (matching.TryGetValue(c, out opening))
                    {
                        char actual;
                        if (!open.TryPop(out actual) || actual != opening)
                        {
                            open.Clear();
                            break;
                        }
                    }
                    else
                    {
                        open.Push(c);
                    }
                }

                long score = 0;
                char o;
                while(open.TryPop(out o))
                    score = score * 5 + points[o];
                if(score != 0)
                    scores.Add(score);
            }
            scores.Sort();
            return scores[scores.Count/2];
        }

        private static Dictionary<char, char> matching = new Dictionary<char, char>
        {
            {')', '(' },
            {']', '[' },
            {'}', '{' },
            {'>', '<' },
        };

        public static int Part1(string[] lines)
        {
            var scores = new Dictionary<char, int>
            {
                {')', 3},
                {']', 57},
                {'}', 1197},
                {'>', 25137},
            };
            int score = 0;
            foreach (var l in lines)
            {
                var open = new Stack<char>();
                foreach (var c in l)
                {
                    char opening;
                    if (matching.TryGetValue(c, out opening))
                    {
                        char actual;
                        if (!open.TryPop(out actual) || actual != opening)
                        {
                            score += scores[c];
                            break;
                        }
                    }
                    else //opening symbol
                    {
                        open.Push(c);
                    }
                }
            }
            return score;
        }

